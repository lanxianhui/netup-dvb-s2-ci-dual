static void start_fw_load(PKSDEVICE device)
{
    /* DL_ADDR_LB=0 DL_ADDR_HB=0 */
    i2c_av_write(device, 0x800, 0x00);
    i2c_av_write(device, 0x801, 0x00);
    // DL_MAP=3 DL_AUTO_INC=0 DL_ENABLE=1
    i2c_av_write(device, 0x803, 0x0b);
    /* AUTO_INC_DIS=1 */
    i2c_av_write(device, 0x000, 0x20);
}

static void end_fw_load(PKSDEVICE device)
{
    /* AUTO_INC_DIS=0 */
    i2c_av_write(device, 0x000, 0x00);
    /* DL_ENABLE=0 */
    i2c_av_write(device, 0x803, 0x03);
}

static BOOL fw_write(PKSDEVICE device, PUCHAR buffer, ULONG len)
{
	struct i2c_msg msg;
	msg.addr	= 0x88 >> 1;
	msg.flags	= 0;
	msg.buf	= buffer;
	msg.len	= len;

	ULONG ret = i2c_transfer(device, 2, &msg, 1);
	return (ret == 1);
}

static void check_fw_load(PKSDEVICE device, ULONG size)
{
    /* DL_ADDR_HB DL_ADDR_LB */
    ULONG s = i2c_av_read(device, 0x801) << 8;
    s |= i2c_av_read(device, 0x800);

    if (size != s)
	{
        KdPrint((LOG_PREFIX "firmware load failed"));
    }
	else
	{
		KdPrint((LOG_PREFIX "loaded firmware (%d bytes)", size));
	}
}

#define FWSEND 48

void CX23885_Load_FW(PKSDEVICE device)
{
	UCHAR gpio_oe = 0, gpio_da = 0;
	UCHAR buffer[FWSEND];
	gpio_oe = i2c_av_read(device, 0x160);
    gpio_da = i2c_av_read(device, 0x164);

	start_fw_load(device);

	buffer[0] = 0x08;
	buffer[1] = 0x02;

	ULONG size = sizeof(cx23885_fw);
	PUCHAR ptr = cx23885_fw;

	while(size > 0)
	{
		ULONG len = ((FWSEND - 2) < size) ? (FWSEND - 2) : size;
		RtlCopyMemory(buffer + 2, ptr, len);

		if(!fw_write(device, buffer, len + 2))
		{
			KdPrint((LOG_PREFIX "firmware load failed"));
			return;
		}

		size -= len;
		ptr += len;
	}

	end_fw_load(device);

	i2c_av_write(device, 0x160, gpio_oe);
    i2c_av_write(device, 0x164, gpio_da);

	check_fw_load(device, sizeof(cx23885_fw));
}

